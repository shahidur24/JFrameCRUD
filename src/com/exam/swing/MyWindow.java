    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        // TODO add your handling code here:
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentdb", "root", "idb@123456");
            pstmt = con.prepareStatement("INSERT INTO student VALUES(?,?,?,?)");
            pstmt.setInt(1, Integer.parseInt(txtId.getText()));
            pstmt.setString(2, txtName.getText());
            pstmt.setString(3, txtEmail.getText());
            pstmt.setString(4, txtRound.getText());

            int n = pstmt.executeUpdate();
            showData();
        } catch (java.sql.SQLIntegrityConstraintViolationException e) {
            JOptionPane.showMessageDialog(this, "Duplicate entry!");
        } catch (Exception e) {
            e.printStackTrace();
        } finally {

            try {
                pstmt.close();
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(MyWindow.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_bSaveActionPerformed

