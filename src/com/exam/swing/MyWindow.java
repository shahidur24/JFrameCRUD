    private void showData() {
        st = new ArrayList<>();
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentdb", "root", "idb@123456");
            pstmt = con.prepareStatement("SELECT * FROM student");
            rs = pstmt.executeQuery();
            while (rs.next()) {
                st.add(new Student(rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4)));
            }

            if (!con.equals(null)) {
                System.out.println("no problem");
            } else {
                System.out.println("problem");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        //st.add(new Student(100, "a", "a@gmail.com", "R45"));
        //st.add(new Student(101, "a1", "a1@gmail.com", "R46"));
        model.setRowCount(0);
        for (int i = 0; i < st.size(); i++) {
            Object[] data = {st.get(i).getId(), st.get(i).getName(), st.get(i).getEmail(), st.get(i).getRound()};
            model.addRow(data);
        }

    }

    private void showOneData() {
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentdb", "root", "idb@123456");
            pstmt = con.prepareStatement("SELECT * FROM student WHERE id=?");
            if (!txtId.getText().equals("")) {
                pstmt.setInt(1, Integer.parseInt(txtId.getText()));
                rs = pstmt.executeQuery();
                if (rs.isBeforeFirst() == false) {
                    JOptionPane.showMessageDialog(this, "No Data Found!");
                    txtName.setText("");
                    txtEmail.setText("");
                    txtRound.setText("");
                    System.out.println("No data found for that ID");
                }
                while (rs.next()) {
                    txtId.setText(String.valueOf(rs.getInt(1)));
                    txtName.setText(rs.getString(2));
                    txtEmail.setText(rs.getString(3));
                    txtRound.setText(rs.getString(4));
                }

            } else {
                System.out.println("Enter ID");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
